	<dt>Content, design</dt>
	<dd>Who will write the content and design the site</dd>
</dl>

<h4>Outcome: Specifcation Document</h4>

<h2>Development</h2>
<p>The stage is when the majority of coding will take place. It is when the ideas are turned into code.</p>

<p>A staging or development server maybe set up.</p>

<h4>Outcome: a functional web app ready for launch</h4>

<h2>Deployment</h2>
<p>This is a crucial time as the production server needs to be properly configured and tested, the site needs to be copied to it, databases need to be set up, and then finally, a launch.</p>

