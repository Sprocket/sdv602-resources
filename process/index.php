<p>Documentation can be a vital part of any project. A 'Specification' is a document that oulines the key aspects of the project. It ensures that everyone has realistic expectations of the outcomes. It also set out repsonsibilities.</p>

<dl>
	<dt>Deliverables</dt>
	<dd>The actual products that will be made</dd>
	<dt>Technical Requirements</dt>
	<dd>Requirements such as third party integration, specific tools, etc.</dd>
	<dt>Budget and Timeframe</dt>
